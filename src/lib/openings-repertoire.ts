/**
 * Get the next move from an opening book given the current game position
 * Returns the next UCI move or null if we've exited the book
 */
export function getNextOpeningMove(
  currentMoves: string[], // UCI moves played so far
  opening: OpeningVariation
): string | null {
  const bookMoves = opening.moves;
  const moveIndex = currentMoves.length;
  
  // Check if we're still in book
  if (moveIndex >= bookMoves.length) {
    return null; // Out of book
  }
  
  // Verify all previous moves match the book
  for (let i = 0; i < moveIndex; i++) {
    if (currentMoves[i] !== bookMoves[i]) {
      return null; // Deviated from book
    }
  }
  
  return bookMoves[moveIndex];
}

/**
 * Check if the current position is still following the opening book
 */
export function isInOpeningBook(
  currentMoves: string[], // UCI moves played so far
  opening: OpeningVariation
): boolean {
  const bookMoves = opening.moves;
  
  // If we've played more moves than the book has, we're out
  if (currentMoves.length >= bookMoves.length) {
    return false;
  }
  
  // Verify all moves match
  for (let i = 0; i < currentMoves.length; i++) {
    if (currentMoves[i] !== bookMoves[i]) {
      return false;
    }
  }
  
  return true;
}

/**
 * Convert a chess.js history to UCI moves
 */
export function historyToUci(history: { from: string; to: string; promotion?: string }[]): string[] {
  return history.map(move => {
    return move.from + move.to + (move.promotion || '');
  });
}

/**
 * Detect which opening was played based on game history
 * Returns the best-matching opening variation or null
 */
export function detectOpeningFromMoves(history: { from: string; to: string; promotion?: string }[]): OpeningVariation | null {
  const uciMoves = historyToUci(history);
  
  // Need at least 2 half-moves to identify an opening
  if (uciMoves.length < 2) return null;

  let bestMatch: OpeningVariation | null = null;
  let bestMatchLength = 0;

  for (const op of DEFAULT_REPERTOIRE) {
    // Check how many consecutive moves match
    let matchLen = 0;
    for (let i = 0; i < Math.min(uciMoves.length, op.moves.length); i++) {
      if (uciMoves[i] === op.moves[i]) {
        matchLen++;
      } else {
        break;
      }
    }
    
    // Require at least 2 matching moves, prefer longer matches
    if (matchLen >= 2 && matchLen > bestMatchLength) {
      bestMatch = op;
      bestMatchLength = matchLen;
    }
  }

  // If we found a specific opening, return it
  if (bestMatch) return bestMatch;
  
  // Fallback: Provide basic opening names for first moves
  if (uciMoves.length >= 1) {
    const firstMove = uciMoves[0];
    const secondMove = uciMoves.length >= 2 ? uciMoves[1] : '';
    
    // Create a temporary opening object for basic recognition
    if (firstMove === 'e2e4') {
      if (secondMove === 'e7e5') {
        return { id: 'generic-e4-e5', opening: 'open-game', name: 'Open Game', moves: ['e2e4', 'e7e5'], description: '', playerColor: 'w' };
      }
      if (secondMove === 'c7c5') {
        return { id: 'generic-sicilian', opening: 'sicilian', name: 'Sicilian Defense', moves: ['e2e4', 'c7c5'], description: '', playerColor: 'w' };
      }
      if (secondMove === 'e7e6') {
        return { id: 'generic-french', opening: 'french', name: 'French Defense', moves: ['e2e4', 'e7e6'], description: '', playerColor: 'w' };
      }
      if (secondMove === 'c7c6') {
        return { id: 'generic-caro', opening: 'caro-kann', name: 'Caro-Kann Defense', moves: ['e2e4', 'c7c6'], description: '', playerColor: 'w' };
      }
      return { id: 'generic-e4', opening: 'kings-pawn', name: "King's Pawn Opening", moves: ['e2e4'], description: '', playerColor: 'w' };
    }
    if (firstMove === 'd2d4') {
      if (secondMove === 'd7d5') {
        return { id: 'generic-qgd', opening: 'qgd', name: "Queen's Pawn Game", moves: ['d2d4', 'd7d5'], description: '', playerColor: 'w' };
      }
      if (secondMove === 'g8f6') {
        return { id: 'generic-indian', opening: 'indian', name: 'Indian Defense', moves: ['d2d4', 'g8f6'], description: '', playerColor: 'w' };
      }
      return { id: 'generic-d4', opening: 'queens-pawn', name: "Queen's Pawn Opening", moves: ['d2d4'], description: '', playerColor: 'w' };
    }
    if (firstMove === 'c2c4') {
      return { id: 'generic-english', opening: 'english', name: 'English Opening', moves: ['c2c4'], description: '', playerColor: 'w' };
    }
    if (firstMove === 'g1f3') {
      return { id: 'generic-reti', opening: 'reti', name: 'Réti Opening', moves: ['g1f3'], description: '', playerColor: 'w' };
    }
  }

  return null;
}

